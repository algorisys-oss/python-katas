class Request:
    """Simulated HTTP request with cookies, headers, and form data."""

    def __init__(
        self,
        method: str,
        path: str,
        headers: dict[str, str] | None = None,
        cookies: dict[str, str] | None = None,
        form_data: dict[str, str] | None = None,
    ):
        self.method = method.upper()
        self.path = path
        self.headers = {k.lower(): v for k, v in (headers or {}).items()}
        self.cookies = cookies or {}
        self.form_data = form_data or {}

    def get_header(self, name: str) -> str | None:
        return self.headers.get(name.lower())


class Response:
    """Simulated HTTP response."""

    def __init__(self, body: Any = None, status_code: int = 200):
        self.body = body
        self.status_code = status_code
        self.headers: dict[str, str] = {}
        self._cookies: list[str] = []

    def set_cookie(self, header_value: str) -> None:
        self._cookies.append(header_value)

    @property
    def cookie_headers(self) -> list[str]:
        return list(self._cookies)

    def __repr__(self) -> str:
        return f"Response(status={self.status_code}, body={self.body})"


