def demo_csrf_attacks():
    """Show CSRF attack prevention."""
    print("\n--- Section 3: Attack Prevention ---")
    try:
        secret = "csrf-secret"

        def handler(request: Request) -> Response:
            return Response(body={"transferred": True})

        middleware = CSRFMiddleware(handler, secret)

        # Attack 1: POST without any CSRF token
        req1 = Request("POST", "/transfer")
        resp1 = middleware(req1)
        print(f"  No token: status={resp1.status_code} -- {resp1.body['error']['detail']}")
        assert resp1.status_code == 403

        # Attack 2: POST with cookie but no header/form token
        token = generate_signed_token(secret)
        req2 = Request("POST", "/transfer", cookies={"csrf_token": token})
        resp2 = middleware(req2)
        print(f"  Cookie only (no submit): status={resp2.status_code}")
        assert resp2.status_code == 403

        # Attack 3: POST with mismatched tokens
        token2 = generate_signed_token(secret)
        req3 = Request(
            "POST", "/transfer",
            cookies={"csrf_token": token},
            headers={"X-CSRF-Token": token2},  # Different token!
        )
        resp3 = middleware(req3)
        print(f"  Mismatched tokens: status={resp3.status_code}")
        assert resp3.status_code == 403

        # Attack 4: POST with forged (unsigned) token
        fake_token = "forged_token_value"
        req4 = Request(
            "POST", "/transfer",
            cookies={"csrf_token": fake_token},
            headers={"X-CSRF-Token": fake_token},
        )
        resp4 = middleware(req4)
        print(f"  Forged token: status={resp4.status_code}")
        assert resp4.status_code == 403

        print("  [PASS] CSRF attacks blocked")
    except (AssertionError, TypeError, AttributeError, Exception) as e:
        print(f"  Not yet implemented: {e}")


